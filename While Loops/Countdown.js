// Write a function that takes a number as a parameter and uses a while loop to count down from that number to 1. Log each number to the console.

function countdown(n) {
  // Your code here
  while (n >= 1) {
    console.log(n);
    n--;
  }
}

// Example
countdown(5); // Should log: 5, 4, 3, 2, 1
